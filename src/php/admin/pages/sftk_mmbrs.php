		<li>「カギ」とはそのロックを解く「鍵」とのことです。</li>
		<li>規定の「登録が必要」と言うロックが設定されます。</li>
		<li>そのロックを使うことで、規定の「登録済み」と言うカギがそのコンテンツをアクセスするために必要になります。</li>
